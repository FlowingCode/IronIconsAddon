    /**
     * The HTML resource that contains the editor iconset
     */
    public static final String URL = "frontend://bower_components/iron-icons/editor-icons.html";

    /**
     * The Iconset name, i.e. {@code "editor"}."
     */
    public static final String ICONSET = "editor";

    /**
     * Return the icon name.
     * @return the icon name, i.e. {@code "editor:name"}..
     */
    public String getIconName() {
        return ICONSET+':'+getIconPart();
    }

    private String getIconPart() {
        return this.name().toLowerCase().replace('_', '-');
    }

    /**
     * Create a new {@link IronIcon} instance with the icon determined by the name.
     * @return a new instance of {@link IronIcon} component
     */
    public IronIcon create() {
        return new IronIcon(ICONSET, this.getIconPart());
    }
