    /**
     * The HTML resource that contains the hardware iconset
     */
    public static final String URL = "frontend://bower_components/iron-icons/hardware-icons.html";

    /**
     * The Iconset name, i.e. {@code "hardware"}."
     */
    public static final String ICONSET = "hardware";

    /**
     * Return the icon name.
     * @return the icon name, i.e. {@code "hardware:name"}..
     */
    public String getIconName() {
        return ICONSET+':'+getIconPart();
    }

    private String getIconPart() {
        return this.name().toLowerCase().replace('_', '-');
    }

    /**
     * Create a new {@link IronIcon} instance with the icon determined by the name.
     * @return a new instance of {@link IronIcon} component
     */
    public Icon create() {
        return new Icon(this.getIconPart());
    }

    /**
     * Server side component for {@code HardwareIcons}
     */
    @HtmlImport(HardwareIcons.URL)
    @SuppressWarnings("serial")
    public final static class Icon extends IronIcon {

         Icon(String icon) {
            super(ICONSET, icon);
        }
    }
